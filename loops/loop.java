package loops;

public class loop {
public static void main(String[] args) {
    for(char c = 'A';c<='Z';c++){
        int val = c;
        System.out.print(c+"-"+(val)+" ");
    }
    System.out.println("\n");
    for(char c = 'a';c<='z';c++){
        int val = c;
        System.out.print(c+"-"+val+" ");
    }
}
    
}