package loops.whileloop;

public class NumbersLoop {
    public static void main(String[] args) {
        int a = 0;
        int b = 6;
        System.out.println("Order Loop");
        while (a <5) {
            a++;
            System.out.println(a);
        }
        System.out.println("Reverse Loop");
        while (b !=1) {
            b--;
            System.out.println(b);
        }
    }
}
